package kr.ac.mju;

public class Course {
	private String c_id;
	private String c_name;
	private String c_year;
	private String c_rank;
	private String c_many;
	private String c_grade;

	
	
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_year() {
		return c_year;
	}
	public void setC_year(String c_year) {
		this.c_year = c_year;
	}
	public String getC_rank() {
		return c_rank;
	}
	public void setC_rank(String c_rank) {
		this.c_rank = c_rank;
	}
	public String getC_many() {
		return c_many;
	}
	public void setC_many(String c_many) {
		this.c_many = c_many;
	}
	public String getC_grade() {
		return c_grade;
	}
	public void setC_grade(String c_grade) {
		this.c_grade = c_grade;
	}
	

}
